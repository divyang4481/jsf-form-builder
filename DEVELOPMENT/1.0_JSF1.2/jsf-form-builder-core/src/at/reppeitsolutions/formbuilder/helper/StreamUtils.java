package at.reppeitsolutions.formbuilder.helper;

import java.io.IOException;
import java.io.InputStream;

/**
 *
 * @author Mathias Reppe <mathias.reppe@gmail.com>
 */
public class StreamUtils {

    public static byte[] getBytes(InputStream is, long length) throws IOException {
        // You cannot create an array using a long type.
        // It needs to be an int type.
        // Before converting to an int type, check
        // to ensure that file is not larger than Integer.MAX_VALUE.
        if (length > Integer.MAX_VALUE) {
            throw new IOException("Inputstream too large.");
        }

        // Create the byte array to hold the data
        byte[] bytes = new byte[(int) length];

        // Read in the bytes
        int offset = 0;
        int numRead = 0;
        while (offset < bytes.length
                && (numRead = is.read(bytes, offset, bytes.length - offset)) >= 0) {
            offset += numRead;
        }

        // Ensure all the bytes have been read in
        if (offset < bytes.length) {
            throw new IOException("Could not completely read input stream.");
        }

        // Close the input stream and return bytes
        is.close();
        return bytes;
    }
    
}
